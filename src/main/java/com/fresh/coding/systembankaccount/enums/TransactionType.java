package com.fresh.coding.systembankaccount.enums;


/**
 * Enum representing different types of transactions.
 */
public enum TransactionType {
    DEPOSIT,             // Represents a deposit transaction
    WITHDRAWAL,          // Represents a withdrawal transaction
    AUTOMATIC_DEBIT,     // Represents an automatic debit transaction
    CURRENCY_CONVERSION  // Represents a currency conversion transaction
}
